/***************************************************************************************************************************
 * Exported global variables and functions (to be accessed by other files)
 ***************************************************************************************************************************/

/* The image encryption key. */
uint8_t __attribute__((aligned(8)))  encryption_key[0x100] = {};
/* Specify the nonce data to be used for encryption. Data size is 16 bytes. The MSB 100 bits of the input data are used as the nonce.*/
uint8_t encryption_iv[] = {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x09, 0x00, 0x00, 0x00};
uint8_t                     encrypted_data [ PLAINTEXT_DATA_SIZE] = {0};
size_t                      length_portion;


/*********************************************************************************************************************
 *  @brief       This function reverse the input byte array every 16 byes.
 *  @param[in]   volatile uint8_t *array: input data array
 *  @param[in]   size_t length: length of the input array
 *  @retval      None
 **********************************************************************************************************************/
static void reverse_every_16_bytes(volatile uint8_t *array, size_t length) {
    if (length % 16 != 0) {
         handle_error(APP_ERR, APP_DATA_ARRAY_NOT_MULTIPLE_OF_16);
        }

        for (size_t i = 0; i < length; i += 16) {
            // Reverse each 16-byte block
            for (size_t j = 0; j < 8; ++j) {
                uint8_t temp = array[i + j];
                array[i + j] = array[i + 15 - j];
                array[i + 15 - j] = temp;
            }
        }
}

/*********************************************************************************************************************
 *  @brief       This function initializes mbedtls crypto and generate wrapped AES key.
 *  @param       None
 *  @retval      None
 **********************************************************************************************************************/

bool init_mbedtls_crypto(void)
{
    mbedtls_platform_context    ctx         = {0};
    psa_key_attributes_t        attributes  = PSA_KEY_ATTRIBUTES_INIT;
    psa_status_t psa_status = PSA_SUCCESS;

    psa_status = mbedtls_platform_setup(&ctx);
    handle_error((uint32_t)psa_status, APP_MBEDTLS_SETUP);
    psa_status = psa_crypto_init();
    handle_error((uint32_t)psa_status, APP_PSA_CRYPTO_INIT);

    // Set key attributes
    psa_set_key_usage_flags(&attributes, PSA_KEY_USAGE_ENCRYPT | PSA_KEY_USAGE_DECRYPT | PSA_KEY_USAGE_EXPORT);
    psa_set_key_algorithm(&attributes, alg);
    psa_set_key_type(&attributes, PSA_KEY_TYPE_AES_WRAPPED);

    /* this should use the size of the plaintext key size*/
    psa_set_key_bits(&attributes, AES128_KEY_BITS);
    psa_set_key_lifetime(&attributes, PSA_KEY_LIFETIME_VOLATILE);

    psa_status = psa_generate_key(&attributes, &key_handle);
    handle_error((uint32_t)psa_status, APP_PSA_GENERAT_KEY);

    psa_status = psa_export_key(key_handle, encryption_key, sizeof(encryption_key),
                          &exported_AES_128_Keylength);
    handle_error((uint32_t)psa_status, APP_PSA_EXPORT_KEY);

   return true;
}

/*********************************************************************************************************************
 *  @brief       This function performs the following three actions in sequence:
 *               1. reverses the plaintext data every 16 byte block before the encryption
 *               2. encrypts the plaintext data
 *               3. reverses the encryption result every 16 byte block
 *  @param       None
 *  @retval      None
 **********************************************************************************************************************/

void AES_CTR_encryption(void)
{
    psa_cipher_operation_t      operation_1             = PSA_CIPHER_OPERATION_INIT;
    psa_status_t                psa_status;
    /* Match the way SKMT and DOTF handles the data. */
    reverse_every_16_bytes(ospi_data_to_be_encrypted, sizeof(ospi_data_to_be_encrypted));
    // Initialize the encryption
    operation_1 = psa_cipher_operation_init ();
    psa_status = psa_cipher_encrypt_setup ( &operation_1, key_handle, alg );
    handle_error((uint32_t)psa_status, APP_PSA_CIPHER_ENC_SETUP);
    /* Set the initial counter */
    psa_status = psa_cipher_set_iv ( &operation_1, (uint8_t *)encryption_iv, sizeof(encryption_iv) );
    handle_error((uint32_t)psa_status, APP_PSA_CIPHER_SET_IV);
    // Encrypt plain text
    encrypted_length = 0;
    psa_status = psa_cipher_update ( &operation_1, (uint8_t *)ospi_data_to_be_encrypted, sizeof(ospi_data_to_be_encrypted), encrypted_data, sizeof(encrypted_data), &length_portion);
    handle_error((uint32_t)psa_status, APP_PSA_CIPHER_UPDATE);
    encrypted_length += length_portion;

    // Finish the encryption.  Set *output and output_size to account for the data that has been encrypted.
    psa_status = psa_cipher_finish ( &operation_1, encrypted_data + encrypted_length, sizeof(encrypted_data) - encrypted_length, &length_portion );
    handle_error((uint32_t)psa_status, APP_PSA_CIPHER_FINISH);

    encrypted_length += length_portion;

    /* Match the way SKMT and DOTF handles the data. */
    reverse_every_16_bytes(encrypted_data, sizeof(encrypted_data));

}




