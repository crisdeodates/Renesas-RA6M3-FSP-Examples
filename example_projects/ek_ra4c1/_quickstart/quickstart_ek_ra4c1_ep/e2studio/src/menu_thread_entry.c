/**********************************************************************************************************************
 * Function Name: menu_thread_entry
 * Description  : Main Menu entry function to prepare and contain the main thread loop.
 * Argument     : pvParameters
 *                Note: pvParameters contains TaskHandle_t
 * Return Value : NONE
 *********************************************************************************************************************/
void menu_thread_entry(void *pvParameters)

{
    FSP_PARAMETER_NOT_USED (pvParameters);
    jlink_console_init();
    common_init();

    while (1)
    {
        vTaskDelay (1);
        main_display_menu();
    }
}
