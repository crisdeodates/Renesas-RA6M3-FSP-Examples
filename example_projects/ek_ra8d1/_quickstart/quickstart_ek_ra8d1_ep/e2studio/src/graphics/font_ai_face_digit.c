/**********************************************************************************************************************
 * Function Name: get_ai_face_font
 * Description  : .
 * Arguments    : index
 *              : col
 *              : result
 * Return Value : .
 *********************************************************************************************************************/
void get_ai_face_font (uint32_t index, d2_color col, st_gimp_ai_face_num_image_t * result)
{
    uint32_t chosen = AI_FACE_DIGIT_NUMBER_IMAGES; // Default return ai_face_percent

    if (index < AI_FACE_DIGIT_NUMBER_IMAGES)
    {
        chosen = index;
    }

    memcpy(result, ai_face_font[chosen], sizeof(st_gimp_ai_face_num_image_t));

    if (col != 0xFFFFFF)
    {
        uint8_t * buf_ptr = result->pixel_data;
        uint32_t  bpp     = result->bytes_per_pixel;
        uint16_t  rgb565  = rgb888torgb565((uint8_t *) &col);

        uint8_t upper = (uint8_t) (rgb565 >> 8);
        uint8_t lower = (uint8_t) (rgb565 & 0xFF);

        for (uint32_t v = 0; v < result->height; v++)
        {
            for (uint32_t h = 0; h < (result->width * bpp); h += bpp)
            {
                if (buf_ptr[0] != 0)
                {
                    buf_ptr[1] = upper; // GGGBBBBB
                    buf_ptr[0] = lower; // RRRRRGGG
                }

                buf_ptr += 2;
            }
        }
    }
}

