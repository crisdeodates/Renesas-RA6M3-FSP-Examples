/*******************************************************************************************************************//**
 * main() is generated by the RA Configuration editor and is used to generate threads if an RTOS is used. This function
 * is called by main() when no RTOS is used.
 **********************************************************************************************************************/
void hal_entry(void)
{
    fsp_err_t err = FSP_SUCCESS;
    fsp_pack_version_t version = {RESET_VALUE};

    /* Version get API for FLEX pack information */
    R_FSP_VersionGet(&version);

    /* Example Project information printed on the Console */
    APP_PRINT(BANNER_1);
    APP_PRINT(BANNER_2);
    APP_PRINT(BANNER_3, EP_VERSION);
    APP_PRINT(BANNER_4, version.version_id_b.major, version.version_id_b.minor, version.version_id_b.patch);
    APP_PRINT(BANNER_5);
    APP_PRINT(BANNER_6);

    APP_PRINT("\r\n\r\nThe project initializes the UART with baud rate of 115200 bps and GPT in the PWM mode.");
    APP_PRINT("\r\nUsing a Terminal Program (like Tera Term), the user can provide a value & press enter key");
    APP_PRINT("\r\nto set the intensity of the on-board LED, The range of input values are displayed");
    APP_PRINT("\r\non the J-Link RTT Viewer. Any failure will also be displayed using J-Link RTT Viewer.");
    APP_PRINT("\r\nTo see the user input values on the Serial Terminal, enable the local echo option.");
    APP_PRINT("\r\n\r\nOpen the Serial Terminal, set the correct baud rate and");
    APP_PRINT("\r\nProvide Input ranging from 1 - 100 to set LED Intensity\r\n");

    /* Initialize GPT in PWM mode */
    err = gpt_initialize();
    if (FSP_SUCCESS != err)
    {
        APP_PRINT("\r\n** GPT TIMER INIT FAILED **\r\n");
        APP_ERR_TRAP(err);
    }

    /* Start GPT */
    err = gpt_start();
    if (FSP_SUCCESS != err)
    {
        APP_PRINT("\r\n** GPT START FAILED **\r\n");
        timer_gpt_deinit();
        APP_ERR_TRAP(err);
    }

    /* Initialize UART */
    err = uart_initialize();
    if (FSP_SUCCESS != err)
    {
        APP_PRINT("\r\n** UART INIT FAILED **\r\n");
        timer_gpt_deinit();
        APP_ERR_TRAP(err);
    }

    /* The user defined function to demonstrate UART functionality */
    err = uart_ep_demo();
    if (FSP_SUCCESS != err)
    {
        APP_PRINT("\r\n** UART EP Demo FAILED **\r\n");
        timer_gpt_deinit();
        deinit_uart();
        APP_ERR_TRAP(err);
    }

#if BSP_TZ_SECURE_BUILD
    /* Enter non-secure code */
    R_BSP_NonSecureEnter();
#endif
}

