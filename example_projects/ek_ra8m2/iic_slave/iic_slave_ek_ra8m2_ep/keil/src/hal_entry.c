/*******************************************************************************************************************//**
 * main() is generated by the RA Configuration editor and is used to generate threads if an RTOS is used. This function
 * is called by main() when no RTOS is used.
 **********************************************************************************************************************/
void hal_entry(void)
{
    fsp_err_t          err     = FSP_SUCCESS;
    fsp_pack_version_t version = {RESET_VALUE};

    /* Get version API for FLEX pack information */
    R_FSP_VersionGet(&version);

    /* Example project information is printed on the Console */
    APP_PRINT(BANNER_1);
    APP_PRINT(BANNER_2);
    APP_PRINT(BANNER_3, EP_VERSION);
    APP_PRINT(BANNER_4, version.version_id_b.major, version.version_id_b.minor, version.version_id_b.patch);
    APP_PRINT(BANNER_5);
    APP_PRINT(BANNER_6);
    APP_PRINT(EP_INFO);

    /* Initialize I2C Master and Slave drivers */
    err = init_i2C_driver();

    /* Handle error */
    if (FSP_SUCCESS != err)
    {
        /* I2C Master and Slave initialization failed */
        APP_ERR_PRINT("\r\n** I2C Master/Slave driver initialization failed **\r\n");

        /* Turn ON LED */
        set_led(LED_ON);

        APP_ERR_TRAP(err);
    }

    while (true)
    {
        /* Perform I2C Slave read and write operations */
        err = process_slave_WriteRead();

        /* Perform cleanup on I2C Slave read/write operation failure.
         * Note: For demonstration purposes, the failure ends with a TRAP.
         * Note: This can be handled in many ways as per the Application needs */
        if (FSP_SUCCESS != err)
        {
            /* Print RTT message */
            APP_ERR_PRINT("\r\n** I2C Slave transaction failed, closing opened modules **\r\n");

            /* De-initialize the opened I2C module */
            deinit_i2c_driver();
            APP_ERR_TRAP(err);
        }

        /* 1-second delay to observe LED toggling between successive read and write operations */
        R_BSP_SoftwareDelay(TOGGLE_DELAY, BSP_DELAY_UNITS_MILLISECONDS);
    }

#if BSP_TZ_SECURE_BUILD
    /* Enter non-secure code */
    R_BSP_NonSecureEnter();
#endif
}

