/*******************************************************************************************************************//**
 * @brief     Example function demonstrating SHA-256 hashing using the NetX Crypto API.
 * @param[in] None
 * @retval    NX_CRYPTO_SUCCESS on success;
 *            other error codes indicate failure.
 **********************************************************************************************************************/
static uint32_t netx_secure_crypto_sha256_example(void)

{
    size_t actual_hash_len = RM_NETX_SECURE_CRYPTO_EXAMPLE_SHA256_HASH_SIZE_LEN;
    uint8_t actual_hash[RM_NETX_SECURE_CRYPTO_EXAMPLE_SHA256_HASH_SIZE_BYTES] = {RESET_VALUE};
    uint8_t metadata[sizeof(NX_CRYPTO_SHA256)] = {RESET_VALUE};
    uint32_t metadata_size = sizeof(NX_CRYPTO_SHA256);
    void *handler = NX_CRYPTO_NULL;
    uint32_t status = NX_CRYPTO_SUCCESS;

    /* Initialize the crypto platform, including SCE and TRNG */
    status = _nx_crypto_initialize();
    if(NX_CRYPTO_SUCCESS != status)
    {
        PRINT_ERR_STR("SHA-256: _nx_crypto_initialize failed");
        return status;
    }

    PRINT_INFO_STR("SHA-256 algorithm initialized and started successfully");

    /* Initialize the SHA-256 method */
    status = _nx_crypto_method_sha256_init(&crypto_method_sha256, NX_CRYPTO_NULL, NX_CRYPTO_NULL,
                                           &handler, metadata, metadata_size);
    if(NX_CRYPTO_SUCCESS != status)
    {
        PRINT_ERR_STR("SHA-256: _nx_crypto_method_sha256_init failed");
        return status;
    }
    /* Start SHA-256 hash operation (initialize internal state) */
    status = _nx_crypto_method_sha256_operation(NX_CRYPTO_HASH_INITIALIZE, handler, &crypto_method_sha256,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL, NX_CRYPTO_NULL, metadata, metadata_size,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL);
    if(NX_CRYPTO_SUCCESS != status)
    {
        PRINT_ERR_STR("SHA-256: Hash initialization failed");
        return status;
    }
    /* Update SHA-256 with the input message.
     * This can be called multiple times to hash message in chunks. */
    status = _nx_crypto_method_sha256_operation(NX_CRYPTO_HASH_UPDATE, handler, &crypto_method_sha256,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL, (uint8_t*) NIST_SHA256ShortMsgLen200,
                                                sizeof(NIST_SHA256ShortMsgLen200),
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL, NX_CRYPTO_NULL, metadata, metadata_size,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL);
    if(NX_CRYPTO_SUCCESS != status)
    {
        PRINT_ERR_STR("SHA-256: Hash update failed");
        return status;
    }
    /* Finalize the hash and get the resulting digest */
    status = _nx_crypto_method_sha256_operation(NX_CRYPTO_HASH_CALCULATE, handler, &crypto_method_sha256,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL,
                                                (uint8_t*) actual_hash, actual_hash_len, metadata, metadata_size,
                                                NX_CRYPTO_NULL,
                                                NX_CRYPTO_NULL);
    if(NX_CRYPTO_SUCCESS != status)
    {
        PRINT_ERR_STR("SHA-256: Hash calculation failed");
        return status;
    }
    /* Verify that generated hash matches the expected digest */
    status = (uint32_t) memcmp(&actual_hash[ZERO], &NIST_SHA256ShortMsgLen200_expected[ZERO], actual_hash_len);
    if(RESET_VALUE != status)
    {
        PRINT_ERR_STR("SHA-256: Generated SHA-256 hash does not match the expected digest");
        return status;
    }
    /* Clean up the SHA-256 metadata */
    status = _nx_crypto_method_sha256_cleanup(metadata);
    if(NX_CRYPTO_SUCCESS != status)
    {
        PRINT_ERR_STR("SHA-256: _nx_crypto_method_sha256_cleanup failed");
        return status;
    }
    PRINT_INFO_STR("SHA-256 algorithm executed successfully");
    return status;
}

